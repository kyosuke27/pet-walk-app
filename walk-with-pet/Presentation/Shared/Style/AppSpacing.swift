import SwiftUI

/// PawTrail全体で使用する余白定義。
enum AppSpacing {
    /// 画面左右の標準余白。
    static let screen: CGFloat = 20
    /// カード内の標準余白。
    static let card: CGFloat = 18
    /// 小さな要素間の余白。
    static let small: CGFloat = 8
    /// 標準的な要素間の余白。
    static let medium: CGFloat = 14
    /// 大きな要素間の余白。
    static let large: CGFloat = 24
}
